<h3>what?</h3>


<p><b>dot-art Ltd</b> is an art gallery and consultancy based in Liverpool city centre, specialising in 
affordable, high quality, locally sourced art, on a sale, commission and rental basis, as well 
as offering a range of complementary services including; art consultancy, exhibition planning and 
implementation, bespoke framing, art valuation and restoration and artist sourcing.
</p>

<p><b>dot-art Services CIC</b> is a not-for-profit organisation which exists to support North-West based visual artists, 
both emerging and established, through a range of services and activities designed to help them develop their careers 
and gain exposure and promotion for their work. A Social Enterprise with artists at it's heart, dot-art Services CIC 
functions in a unique fashion, operating a membership system which gives artists a range of  benefits including their 
own web page, access to exhibition, career development and promotional opportunities, as well as help and advice, 
networking events and discounts on art supplies and framing.</p>

<p><a href="http://services.dot-art.com">More information about our support services for artists</a></p>


<h3>how?</h3>

<p>dot-art works with over 100 artists to provide an unrivaled choice of affordable, high quality, 
locally sourced art work for home or business, both through our city centre showroom, external exhibitions and comprehensive website. 
In addition we source and supply artists for a wide range of other projects, including workshops, training and
community participation events.</p>

<p>We offer a comprehensive, flexible and affordable service to our corporate clients, 
from consultancy and sourcing to framing and hanging. Work can be acquired on a purchase, commission or rental 
basis, and can be built into wider marketing, branding or CSR objectives using a variety of bespoke approaches, 
including exhibitions, artist talks, promotional launches or events and staff workshops.</p>


<h3>who?</h3>

<p>dot-art was founded by Lucy Byrne in 2006. An Art History graduate from the University of Liverpool, Lucy
has a wealth of experience in planning, developing and implementing a wide range of visual art projects, coupled with 
a extensive contact network of artists and partners across the North West. 


