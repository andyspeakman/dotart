<h3 class="artist">case study</h3>

<p class="artist">Nigel Brook, Managing Director of Kier North West, discusses working with dot-art:</p>

<ul class="artist">

<li class="artist"><b>What initially attracted you to working with dot-art?</b></li>
<li class="artist">The initial attraction was moving our office headquarters to Speke and the appeal of supporting local 
Merseyside Artists blended well with the dual purpose of our corporate responsibility and enhancing our office with a 'local flavour'.
</li>

<li class="artist"><b>Did you look at any alternative options?</b></li>
<li class="artist">I found the dot art website easy to understand and provided a great choice of styles and therefore 
we didn't look any further - the alternative was to include pictures of our finished buildings which we have done - 
but combining this with the artists work has proven very successful.
</li>

<li class="artist"><b>What did you hope to gain from the project?</b></li>
<li class="artist">We wanted the office to feel as though it belonged to Liverpool and had that 'obvious' feel 
which we have achieved as well as having a talking point for visitors.
</li>

<li class="artist"><b>Do you feel this has been achieved?</b></li>
<li class="artist">Yes as the choice of artwork available really suited our vision of what we were looking for.</li>

<li class="artist"><b>How did you find the experience of working with dot-art?</b></li>
<li class="artist">dot-art were very professional in their approach right from the outset they tracked the fit out period with us, 
listened to what we were trying to achieve and put forward some fantastic proposals which were diverse enough to stir 
the imagination and enable us to make a choice which has resulted in a striking original painting in our Boardroom and 
brought some colour and life to other areas of our office.</li>

<li class="artist"><b>What additional benefits have you seen as a result of the project?</b></li>
<li class="artist">We certainly get comments from our visitors who are also then pleased to learn that the artwork is 
by local artists and it has also shown our staff that we all need to be a bit more free thinking in todays business environment.
</li>

<li class="artist"><b>Do you have any other comments?</b></li>
<li class="artist">The advice we received from Lucy at dot-art was very beneficial in helping us to decide on our final 
choices; she has a good understanding of the corporate environment and really listened to what we were trying to achieve. 
I would be very happy to recommend dot-art's products and services.</li>

</ul>


