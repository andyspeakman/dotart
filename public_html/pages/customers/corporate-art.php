<h3 class="artist">buy, rent or commission art for your offices</h3>

<p class="artist">If you are considering acquiring art for your offices, dot-art's art consultants can help.</p>

<h3 class="artist">why?</h3>

<p class="artist">Studies have again and again proved the huge benefits that collecting and commissioning
art can have on a business. The immediate advantage is the image that well placed, quality art
 conveys not only to the stakeholders of a company, but to staff, customers, suppliers and the wider
 community. Art can have a positive impact on the workforce, inspiring and motivating them through
 the working environment. The inclusion of art and therefore the support of local artists can also
 contribute to corporate social responsibility objectives and enhance your companys reputation in the
  community.</p>
  
<h3 class="artist">what?</h3>

<p class="artist">dot-art can help you choose the right pieces of art for your offices and your budget.
 Whether you choose to <b>buy</b> a small print or a <b>commission</b> a unique centrepiece for a boardroom, our close
  relationship with our wide range of artists will ensure that your needs can be met and exceeded.</p>
  
<p class="artist">If you require art for a limited amount of time, perhaps for a showhome, exhibition or
event, you can <b>rent</b> art, with prices starting at just £5 per month. After an agreed period, we will
 then rotate your chosen artwork, ensuring your working enviroment is always fresh and interesting.</p>

