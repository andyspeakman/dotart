<h3>Alistair J Parker Exhibition</h3>

<img src="/images/festival-tn.jpg" title="Liverpool Design Festival Logo" alt="Liverpool Design Festival Logo"/>
<img src="http://www.dot-art.co.uk/artists%20work/alistair%20parker/beatles-girls_tn.jpg" title="Alistair J Parker" alt="Alistair J Parker"/>

<p>As part of the 2011 Liverpool Design Festival, dot-art is delighted to be exhibiting a selection of prints by nationally and 
internationally exhibited artist and photographer Alistair J Parker, whose striking and intriguing work is represented by galleries 
in London, Manchester and Liverpool.</p>

<p>Alistair's unique fine art style explores process and media with a particular emphasis on photography, digital process and 
experimental mixed media techniques. This results in work with a unique finish and a hauntingly contemporary appeal, increasingly 
sought after by interior designers and design professionals as well as personal buyers. The adaptability of Alistair's work is 
demonstrated by its use in wallpaper, and lifestyle accessories and inclusion in advertising and TV commercials.</p>

<p>The artist says: “My work as an artist draws heavily on my experience as a photographer, bringing together a fusion of photography, 
digital imaging and fine art. The approach taken is highly experimental and combines a range of unconventional materials and processes 
in addition to the more traditional art media. Subjects are many and varied, my current work involves the observation of people and 
urban space they occupy, together with a fascination for the rapidly decaying and disappearing urban scene, particularly the classic 
seaside resort. My current work is influenced by the crossover between street art and fine art."</p>

<p>The exhibition is open to the public from 12-6pm Monday to Friday and by appointment, at dot-art, 16 Queen Avenue, Castle Street, Liverpool, L2 4TX.</p>

<p><a href="http://www.dot-art.co.uk/artist/alistair-parker">View Alistair's portfolio.</a></p>
