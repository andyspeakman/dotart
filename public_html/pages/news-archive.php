<h3>20 Shades of Collett</h3>

<a><img src="http://www.dot-art.co.uk/images/20-Shades-of-Collett.jpg" title="20 Shades of Collett" alt="20 Shades of Collett"/></a>

<p>The first solo show at the gallery by Liverpool-based artist Stephen Collett, "20 Shades of Collett" 
is a collection of portraits featuring the people who have most inspired him. The portraits have been produced 
using a range of innovative methods to reflect the characteristics of the figures portrayed. 
William Shakespeare has been created with words rather than brush strokes; Derren Brown emerges from an 
optical illusion and Sir Richard Branson has been painted using a credit card rather than a brush.</p>

<p>The exhibition runs at the Williamson Art Gallery & Museum, Birkenhead, from 8th June - 7th July 2013.<br> 
(Opening times: Tuesday – Sunday: 10am – 5pm, Monday: closed)</p>

