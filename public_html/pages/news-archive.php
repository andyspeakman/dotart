and hope that, away from the hustle and bustle of the working week, people will take the time to consider the alternatives 
to jumping in the car at the weekend. The competition's only two criteria are that the photographs must capture both a popular 
Merseyside tourist site and sustainable travel. Whether it's a bike ride to the Dream sculpture in St Helens, 
or a morning stroll past the Gormley statues in Crosby or along New Brighton seafront, there is inspiration right across 
the region and we can't wait to see what budding local photographers produce when asked to capture the best of 
what Merseyside has to offer."</p>

