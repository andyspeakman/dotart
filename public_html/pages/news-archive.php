<h3>dot-art Schools Programme Launches</h3>

<a><img src="http://www.dot-art.co.uk/images/dot-art-schools-small-banner.jpg" title="dot-art Schools" alt="dot-art Schools"/></a>

<p>Twenty local schools are currently taking part in the first ever dot-art Schools programme,
an interactive, online, inter-school art competition for Year 6 and Year 9 students,
supported by Arts Council England. Voting is open until the end of May, and the winning
pieces from each participating school will be shown in an exhibition in Liverpool city
centre in July</p>

<p>The dot-art Schools programme is an innovative new scheme giving students a public platform to
display their art work outside the school environment and the chance to enter into friendly interschool interaction and competition. Each participating school selects 25 pieces of work which are
put on display via the <a href="http://schools.dot-art.com/">dot-art Schools website</a>, where students, family and friends, as well as the
public, are able to vote for their favourite piece. An exhibition will then be held showcasing the top
works from each participating school in the Domino Gallery, Liverpool, a scholarship awarded to
the top student in the program, and certificates awarded to the top students at each school.</p>

<p>dot-art Schools is currently looking for both Primary and Secondary schools across the Liverpool
City Region who would like to take part in the next programme, which starts in September.
Teachers interested in getting involved can find out more and complete an Expression of Interest
form on the dot-art Schools website: schools.dot-art.com/what.</p> 

<p><b>How To Vote:</b></p>

<ul>
