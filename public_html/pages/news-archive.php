<h3>Art @ The Orchard - Un / Familiar</h3>

<img src="/images/orchard3c.jpg" title="Art @ The Orchard - Un / Familiar" alt="Art @ The Orchard - Un / Familiar"/>

<p>A look at the work of two artists - Matthew Thomas and Andrew Collin - who take familiar objects and images, and manipulate them in various ways to create new, 
interesting and often visually confusing works of art.</p>

<p>Andrew is an experimental, semi-abstract photographer whose images deliberately
confuse 2D and 3D surfaces, to create ambiguity. He particularly tries to create original
work, often from the simplest objects and scenes. The works are produced in ongoing
series, and new series are introduced if experiments produce results of interest.</p>

<p>Matthew is a photographer who merges the “decisive moment” with concept and
montage. He says: “I use photography and digital imaging technologies to create
visceral layers that question historical and cultural concepts of time and space. Each
project often consists of multiple works, grouped around specific themes and meanings.
During research and production new areas of interest arise and lead to the next body of
work... The concrete, repetitive nature of this work frees my imagination and provides
many opportunities for a "happy accident" to influence the finished product.”</p>

<p>Exhibition runs 11th June - 20th August 2010</p>
