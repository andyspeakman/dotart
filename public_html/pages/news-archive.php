<h3>auschwitz, echoes of sorrow</h3>
<img src="/images/auschwitz/flyer_tn.jpg"  title="auschwitz, echoes of sorrow" alt="auschwitz, echoes of sorrow"/>

<p>from january 26th to february 8th 2007, the holiday inn, lime street, liverpool, hosted a photographic
exhibition by dot-art artist, john guy, entitled, "auschwitz, echoes of sorrow".</p>
<p>sixty years on, the grim legacy of the auschwitz death camp still resonates through history.
with holocaust memorial day (january 27th) imminent, liverpool photographer john guy seeks to
document the scene of one of the world’s most hideous acts by going beyond the clichés. so often shot
solely in black and white, auschwitz echoes of sorrow features startling colour photographs mixed with
atmospheric mono pictures of the camp’s buildings, surroundings and the artefacts now preserved within it.
the vivid blues of the polish winter sky above the camp’s iconic gates contrast with the gloomy
atmosphere of sepia photographs taken deep in the complex. juxtaposing close-ups of symbols of captivity
and cruelty against wider portraits of the surrounding scenery creates an account of auschwitz that stands
out in its sincerity and depth.
</p>
<p>see the whole exhibition on <a href="/pages/artists/artist.php?artistId=78">john guys page.</a></p>

