generating a commercial income whilst also having a positive impact on the environment and the community.</p>

<p>The Future 100 Awards were first launched in 2008 to recognise and profile young entrepreneurs running businesses, 
which have a triple bottom line. The definition of a triple bottom line, is a business that addresses people, planet 
and profit. They have a strong commercial foundation as well as a significant social and environmental impact. 
The Future 100 Awards recognises the success of young entrepreneurs aged 18-35 who demonstrate entrepreneurial 
flair and innovation in progressing a responsible business venture. Our definition of responsible business, 
is one which demonstrates an equal balance between economic, environmental and social goals to achieve ultimate business success. 
The future of our world is in the hands of individuals who are committed to generating commercial and ethical returns. 
Challenging economic times can offer opportunities to question the way we operate as both a business community and a society. 
We award the ‘Future 100' young entrepreneurs who are changing the face of everyday business and improve commerce’s impact on the wider world.</p>

