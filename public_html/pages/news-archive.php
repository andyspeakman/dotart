<h3>Art @ The Orchard - People, Places, Faces</h3>

<img src="http://eventbrite-s3.s3.amazonaws.com/eventlogos/81934/figuresoncanvas.jpg" title="Art @ The Orchard - People, Places, Faces" alt="Art @ The Orchard - People, Places, Faces"/>

<p>Following on from the success of the initial exhibition by James Lavott, the 

second Art @ The Orchard show will feature the work of two painters with 

very different approaches to the portrayal of the human figure. Gary Beach 

and Susan Finch are the latest artists to be featured in this innovative 

series of exhibitions in collaboration with local arts organisation dot-art, 

showcasing the work of local artists and exposing their work to a wide 

audience, in relaxed, inviting surroundings.</p>

<p>
Liverpool artist Susan Finch takes a fresh and searching look at the 

figure in her latest collection of paintings. She explores and 

celebrates the subject in vibrant oil colour, bringing out the visceral 

essence of the person she paints. She places the figure within a 

richly expressionistic  landscape, sometimes Liverpool, sometimes an 

imagined setting which reflects the inner life of her subject. Her Beatles 

quartet of paintings use written speech, not only to tell a story, but also 

to bring  an exuberant  dynamic quality to her work.</p>

<p>Gary Beach takes the figure in the urban environment as his subject. His 

work has always been influenced by the human figure, from early interest in 

comic and fashion drawing, he now uses everyday life to capture moods and 

experiences. He says of the work: “The paintings express the hustle and 

bustle and, on a more personal level, the loneliness, isolation and 

anonymity that we feel in life. The city has become a concrete muse- a focus 

to inspire my paintings. Set amongst crowded places with anonymous people 

who pass by everyday. Painting allows me an outlet for my emotions and 

passion to be expressed.”</p>



