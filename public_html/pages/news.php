<h3>dot-art at the Bluecoat, Spring 2014 Art Classes</h3>

<img src="http://www.dot-art.co.uk/images/aboriginal-painting-day.jpg" title="Aboriginal Painting Day" alt="Aboriginal Painting Day"/>

<p>As the weather starts to improve and we all look forward to summer, dot-art’s spring 
series of evening and weekend art classes are a fantastic way of making the most of 
the longer, lighter days and getting your creative juices flowing!</p>

<p>Taking place in the city centre’s creative hub, the Bluecoat, dot-art’s art classes 
provide a range of options for beginners and the more experienced alike to build on their 
talents. This season sees the return of some old favourites, as well as number of 
new courses added to the schedule.</p>

<p>Starting with photography, the first date for your diary is Thursday 3rd April, when our 
Night Photowalk returns. Steward Ellett leads a guided walk around the Pier Head and Albert 
Dock area of Liverpoool looking at different ways of capturing photos in low light, using 
various in camera techniques to best photograph the city at night. This is followed our 
the regular Sunday daytime Photowalk on 18th May.</p>

<p>Our Tuesday evening ten week courses remain hugely popular, so make sure you book early 
for the three options we have starting on 29th April; Richard Meaghan’s Painting course, 
Roy Munday’s Life Drawing course, and the return of Lucy Wilson’s Bookbinding course. 
These are all suitable for beginners.This term also sees the introduction of an Intermediate 
Life Drawing course on a Monday evening (starting on 21st April), for more experienced 
artists and those who have completed our original course. In addition, on Sunday 27th 
April we welcome Rob Edmondson who will lead a one day course on Aboriginal Art, encouraging 
participants to experiment with paint without the use of brushes!</p>

<p>Finally, during the Easter holidays, from Monday 7th to Wednesday 9th April we welcome 
Angela Burton to lead a series of dot-art Stars sessions, offering 8-11 year olds an 
opportunity to follow their own creative ideas, explore a wide variety of materials 
and to be adventurous in their approach.</p>

<p><strong><a href="http://www.dot-art.co.uk/art-classes">Full details, prices and booking information can be found here.</a></strong></p>



