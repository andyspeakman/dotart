<a href="http://www.dot-art.co.uk/artist/mike-greatbanks">Mike Greatbanks,</a>
<a href="http://www.dot-art.co.uk/artist/paul-r-francis">Paul R Francis,</a>
<a href="http://www.dot-art.co.uk/artist/mark-chapman">Mark Chapman</a> and
<a href="http://www.dot-art.co.uk/artist/sarah-blackhall">Sarah Blackhall.</a>
 </p>

<p>But you're probably asking yourself... What exactly is a Concept Store? The thinking behind a "concept" 
store is that it challenges the limitations of a traditional retail unit. It’s like a mini-emporium 
where you can find quirky new products from fun, vibrant, independent designers and retailers.  
It''s not just the sale of physical objects that are on offer but the full-on emotional experiences that 
a store like this can offer. We know that retail is changing, it's no longer just a 
shopping destination but a journey that customers want to be taken on and experience.</p>

<p><b>Come and see for yourself when Rex opens on the 10th October. You can find us at:
