<h3>dot-art at Project Rex</h3>

<img src="http://www.dot-art.co.uk/images/Rex-square.jpg" title=">dot-art at Project Rex" alt=">dot-art at Project Rex"/>

<p>Rex is Liverpool's very first concept and box store, designed around independent retailers to create a shared retail and work 
space for like minded creative businesses. dot-art is delighted to be one of the very first organisations to partner with Rex.
</p>

<p>We will be selling an exclusive range of affordable original and limited edition paintings and prints by our member artists,
which will change on a regular basis. The first featured artists will be: 
<a href="http://www.dot-art.co.uk/artist/john-petch">John Petch,</a>
<a href="http://www.dot-art.co.uk/artist/sophie-green">Sophie Green,</a>
<a href="http://www.dot-art.co.uk/artist/marianthi-lainas">Marianthi Lainas,</a>
<a href="http://www.dot-art.co.uk/artist/jackie-stevens">Jackie Stevens,</a>
