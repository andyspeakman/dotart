<p><b>dot-art Schools</b> is an annual, interactive, online, inter-school art competition, culminating in an exhibition. 
The pilot schemed launched in January 2013, with 20 schools based in the Liverpool City Region taking part 
(both primary and secondary). Each school submitted 25 pieces of work from their Year 6 or Year 9 students respectively; 
these were then added to the dot-art Schools website. During April and May, students, staff, families and the general public 
were able to vote for their favourite artwork through the site. The three pieces with the most votes from each school 
have now been chosen to take part in an exhibition at the Domino Gallery, Liverpool City Centre in July 2013. Each winning student 
will be presented with a certificate, and we will also be announcing the winner of the “dot-art Scholarship”, 
giving one of the Year 9 pupils the opportunity to display their work alongside professional artists for 12 months. 
</p>

